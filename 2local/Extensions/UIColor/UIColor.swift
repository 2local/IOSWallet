import UIKit

extension UIColor {
  convenience init(red: CGFloat, green: CGFloat, blue: CGFloat, alpha: CGFloat = 1) {
    self.init(red: red / 255,
              green: green / 255,
              blue: blue / 255,
              alpha: alpha)
  }
}

